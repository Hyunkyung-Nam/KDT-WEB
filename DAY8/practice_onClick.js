let inputNum1 = prompt("첫번째 숫자를 입력해 주세요.");
let inputNum2 = prompt("두번째 숫자를 입력해 주세요.");

function add() {
    let result = (Number(inputNum1) + Number(inputNum2))
    alert(result) ;
}
function sub() {
    let result = (Number(inputNum1) - Number(inputNum2))
    alert(result) ;
}
function mul() {
    let result = (Number(inputNum1) * Number(inputNum2))
    alert(result) ;
}
function divide() {
    let result = (Number(inputNum1) / Number(inputNum2))
    alert(result) ;
}